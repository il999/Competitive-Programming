#include <bits/stdc++.h>
using ll =long long;
using namespace std;
void solve()
{
    int n;
    cin>>n;
    int space[2*n];
    space[n]=-1;
    int a[n];
    for(int i=0;i<n;i++)
    {
        space[i]=-1;
        cin>>a[i];
        a[i]--;
    }
    for(int i=0;i<2*n;i++)
    {
        space[i]=-1;
    }
    sort(a,a+n);
    ll dp[n+1][2*n+1];
    for(int i=0;i<=n;i++)
    {


        for(int j=0;j<=2*n;j++)
        {
            dp[i][j]=1e9+7;
        }

    }
    dp[0][0]=0;
   for(int i=0;i<=n;i++)
   {
       for(int j=0;j<2*n;j++)
       {
           if(dp[i][j]<1e9+7)
           {


            if(i<n)dp[i+1][j+1]=min(dp[i+1][j+1],dp[i][j]+abs(a[i]-j));
            dp[i][j+1]=min(dp[i][j+1],dp[i][j]);
           }
       }
   }
    cout<<dp[n][2*n-1]<<"\n";
}
int main()
{
    ios_base::sync_with_stdio(0); cin.tie(0);
    int t;
    cin>>t;
    while(t--)
        solve();
}
