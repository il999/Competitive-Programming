#include <bits/stdc++.h>
using ll =long long;
using namespace std;
void solve()
{
    ll n,k;
    cin>>n>>k;
    if(((k-1)*(k))/2<n-1)
    {
        cout<<-1<<"\n";
        return;
    }
    if(n==1)
    {
        cout<<0<<"\n";
        return;
    }
    if(n<=k)
    {
        cout<<1<<"\n";
        return;
    }
    ll low=0;
    ll high=k-1;
    ll mid;
    while(low<high)
    {
       // cout<<"D";
         mid=(low+high+1)/2;
        if((k-1)*(k)/2-(mid*(mid+1))/2+1<n)
        {
            high=mid-1;
        }else
        {
            low=mid;
        }
    }
    cout<<k-(high+1)<<"\n";

}
int main()
{
    ios_base::sync_with_stdio(0); cin.tie(0);

        solve();
}
