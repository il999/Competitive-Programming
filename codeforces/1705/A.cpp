#include <bits/stdc++.h>
using ll =long long;
using namespace std;
void solve()
{
    int n,x;
    cin>>n>>x;
    int l[n*2];
    for(int i=0;i<n*2;i++)
    {
        cin>>l[i];
    }
    sort(l,l+2*n);
    for(int i=0;i<n;i++)
    {
        if(l[i]+x>l[i+n])
        {
            cout<<"NO\n";
            return;
        }
    }
    cout<<"YES\n";

}
int main()
{
    ios_base::sync_with_stdio(0); cin.tie(0);
    int t;
    cin>>t;
    while(t--)
        solve();
}
