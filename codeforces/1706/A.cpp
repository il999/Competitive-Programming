#include <bits/stdc++.h>
using ll =long long;
using namespace std;
void solve()
{
    int n,m;
    cin>>n>>m;
    int l[n];
    string s="";
    for(int i=0;i<m;i++)
    {
        s+='B';
    }
    for(int i=0;i<n;i++)
    {
        cin>>l[i];
    }
    for(int i=0;i<n;i++)
    {
        if(s[min(l[i]-1,m-l[i])]=='A')
        {
            s[max(l[i]-1,m-l[i]) ]='A';
        }else

        {
            s[min(l[i]-1,m-l[i]) ]='A';
        }


    }
    cout<<s<<"\n";
}
int main()
{
    ios_base::sync_with_stdio(0); cin.tie(0);
    int t;
    cin>>t;
    while(t--)
        solve();
}
