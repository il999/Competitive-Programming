#include <bits/stdc++.h>
using ll =long long;
using namespace std;
void solve()
{
    int n,m;
    cin>>n>>m;
    ll l[n][m];
    ll dpmin[n+1][m+1];
    ll dpmax[n+1][m+1];
    for(int i=0;i<n;i++)
    {
        for(int j=0;j<m;j++)
        {
            cin>>l[i][j];
            dpmin[i][j]=1e9+7;
            dpmax[i][j]=-1e9+7;
        }
    }
    dpmin[0][0]=l[0][0];
    dpmax[0][0]=l[0][0];
    for(int i=0;i<n;i++)
    {
        for(int j=0;j<m;j++)
        {
            if(i>0)
            {
                dpmin[i][j]=min(dpmin[i-1][j]+l[i][j],dpmin[i][j]);
                dpmax[i][j]=max(dpmax[i-1][j]+l[i][j],dpmax[i][j]);

            }
            if(j>0)
            {
                dpmin[i][j]=min(dpmin[i][j-1]+l[i][j],dpmin[i][j]);
                dpmax[i][j]=max(dpmax[i][j-1]+l[i][j],dpmax[i][j]);
            }
           // dpmin[i][j]=min(dpmin[i-1][j]+l[i-1][j-1],dpmin[i][j-1]+l[i-1][j-1]);
           // dpmax[i][j]=max(dpmax[i-1][j]+l[i-1][j-1],dpmax[i][j-1]+l[i-1][j-1]);
        }
    }
    if((n + m - 1) % 2 == 0 && dpmin[n-1][m-1]<=0&&dpmax[n-1][m-1]>=0)
    {
        cout<<"YES\n";
       // cout<<dpmin[n-1][m-1]<<" "<<dpmax[n-1][m-1]<<"\n";
    }else
    {
        cout<<"NO\n";
    }

}
int main()
{
    ios_base::sync_with_stdio(0); cin.tie(0);
    int t;
    cin>>t;
    while(t--)
        solve();
}
