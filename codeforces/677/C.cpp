#include <bits/stdc++.h>
using ll =long long;
using namespace std;
const int mod=1e9+7;
void solve()
{
    string s;
    cin>>s;
    int n=s.size();
    long long ans=1;
    int l[64]={729, 243, 243, 81, 243, 81, 81, 27, 243, 81, 81, 27, 81, 27, 27, 9, 243, 81, 81, 27, 81, 27, 27, 9, 81, 27, 27, 9, 27, 9, 9, 3, 243, 81, 81, 27, 81, 27, 27, 9, 81, 27, 27, 9, 27, 9, 9, 3, 81, 27, 27, 9, 27, 9, 9, 3, 27, 9, 9, 3, 9, 3, 3, 1};
    for(int i=0;i<n;i++)
    {
        if(s[i]>='A'&&s[i]<='Z')
        {
            ans*=l[10+s[i]-'A'];
        }else if(s[i]>='a'&&s[i]<='z')
        {
              ans*=l[36+s[i]-'a'];

        }else if(s[i]>='0'&&s[i]<='9')
        {
              ans*=l[s[i]-'0'];
        }else if(s[i]=='-')
        {
                ans*=l[62];
        }else
        {
              ans*=l[63];
        }
        ans%=mod;
    }
    cout<<ans<<"\n";


}
int main()
{
    ios_base::sync_with_stdio(0); cin.tie(0);

        solve();
}
