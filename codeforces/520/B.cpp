#include <bits/stdc++.h>
using ll =long long;
using namespace std;
int m;
   bool seen[10000*2+1];

void solve()
{
    int n;
    cin>>n>>m;
    if(m<n)
    {
        cout<<n-m<<"\n";
        return;
    }

    for(int i=0;i<m*2;i++)
    {
        seen[i]=0;
    }

    queue<array<int,2>> q;
    array<int,2> n1;
    q.push({n,0});
    while(!q.empty())
    {
        n1=q.front();
        q.pop();
        if(n1[0]==m)
        {
            cout<<n1[1]<<"\n";
            return;
        }
        if(n1[0]*2<=m*2&&!seen[n1[0]*2])
        {
            seen[n1[0]*2]=1;
            q.push({n1[0]*2,n1[1]+1});
        }
        if(n1[0]>1&&!seen[n1[0]-1])
        {
            seen[n1[0]-1]=1;
            q.push({n1[0]-1,n1[1]+1});
        }
    }
    //cout<<"DS";
}
int main()
{
    ios_base::sync_with_stdio(0); cin.tie(0);

        solve();
}
